    public boolean verificaIntervalo()
    {
        try
        {
            this.segundos = Integer.parseInt(campoSegundos.getText()) * 1000;
            return true;
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "O campo deve ser num√©rico");
            return false;
        }
    }
    
    public void inicia()
    {
        JOptionPane.showMessageDialog(this, "Iniciando em 10 segundos");
        threadInput.active = true;
        threadInput.segundos = this.segundos;
        threadInput.start();
    }
    
